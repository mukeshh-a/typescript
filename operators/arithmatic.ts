let a = 10;
let b = 3;

let sum = a + b;
console.log("Sum:", sum); // Output: 13

let diff = a - b;
console.log("Difference:", diff); // Output: 7

let product = a * b;
console.log("Product:", product); // Output: 30

let division = a / b;
console.log("Division:", division); // Output: 3.333...

let remainder = a % b;
console.log("Remainder:", remainder); // Output: 1